# Leitura e declaração das variáveis:
# "fahrenheit", que representa a temperatura em Fahrenheit.
fahrenheit = float(input("Digite o grau em Fahrenheit: "))

# Cálculo e declaração da variável "celsius", que representa a temperatura em Celsius.
celsius = ((fahrenheit - 32)/9) * 5

# Imprimir a variável "celsius".
print("O grau em Celsius é: ",celsius, "°C")
