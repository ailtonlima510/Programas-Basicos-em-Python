# Leitura e declaração das variáveis:
# "base", que representa a base do triângulo;
# "altura", que representa a altura do triângulo.
base = float(input("Digite o valor da base: "))
altura = float(input("Digite o valor da altura: "))

# Cálculo e leitura da variável "area", que representa a área do triângulo.
area = (base*altura/2)

# Imprimindo a variável "area"
print("O valor da área é: ", area)
