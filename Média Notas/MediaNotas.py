# Leitura e declaração das variáveis:
# "nota1","nota2" e "nota3", que representam as notas.
nota1 = float(input("Digite a primeira nota: "))
nota2 = float(input("Digite a segunda nota: "))
nota3 = float(input("Digite a terceira nota: "))

# Calculo e declaração da variável "media", que representa a média das notas.
mediaFinal = (nota1*2 + nota2*3 + nota3*5)/10

# Imprimir a variável "mediaFinal".
print("A média final do aluno é: ",mediaFinal)
